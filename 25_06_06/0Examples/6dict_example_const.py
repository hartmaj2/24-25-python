# Example of how a dictionary works

# It works like a real dictionary, you give it a word and it gives you the "translation"

slovnicek = {
    "ahoj":"hello", 
    "nashledanou":"bye",
    1 : "one"
    }

print(slovnicek["ahoj"])
print(slovnicek["nashledanou"])

print(slovnicek[1])

print(slovnicek["hello"])